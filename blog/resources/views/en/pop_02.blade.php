                <b>Article 5. (Destruction of Personal Information)</b><br>
                ① The Promotion Institute shall immediately destroy the personal information after the retention period has expired or the purpose for the collection and use of such personal information has been achieved.<br>
                ② Personal information shall be destroyed as follows:<br>
                1. Procedure: The personal information provided by the User shall be destroyed after a certain period of time required under the internal regulations or the applicable laws.<br>
                2. Method: The personal information stored electronically shall be destroyed in such a manner that it cannot be recovered. Documents containing the personal information shall be shredded by using a shredder.<br><br>
    
                <b>Article 6. (Security of Personal Information)</b><br>
                 ① The User may claim for any damage caused by the Promotion Institute if the latter violates the Personal Information Protection Act. In this case, the Promotion Institute shall take full responsibility unless proven otherwise.<br>
                 ② The Promotion Institute shall take the following technical, administrative, and physical measures to keep personal information secure under Article 29 of the Personal Information Protection Act.<br>
                 1. Establish and implement internal control plans for the security of personal information.<br>
                 2. Minimize the number of staff with access to personal information and designate a manager who is in charge of handling personal information as part of the privacy policy.<br>
                 3. Store and manage personal information in an encrypted form while important data files and transmission are encrypted or locked for additional security protection.<br>
                 4. As part of technical measures against hacking, the Promotion Institute will use and update security software programs to ensure the security of personal information while the system is installed in a restricted area where technical and physical monitoring and protection is available.<br>
                 5. Apply access control on the database system to restrict any access to personal information and implement an intrusion prevention system to deny unauthorized access.<br>
                 6. Retain the access history and record of accessing the personal information protection system for at least 6 months.<br>
                 7. Store documents and auxiliary storage media in a secure place with a locking device.<br>
                 8. Store personal information in a separate location to prevent any authorized access.<br>
                 ③ The User may claim for any damage caused by the Promotion Institute if the latter violates the Personal Information Protection Act. In this case, the Promotion Institute shall take full responsibility unless proven otherwise.<br><br>
                
