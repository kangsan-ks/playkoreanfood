                <b>Article 4. (Rights &amp; Duties of Information Owner and How to Exercise Rights)</b><br>
                ① The information owner may exercise the information protection rights regarding the following at any time.<br>
                 1. Access to Personal Information: Access may be requested under Article 35 (Access to Personal Information) of the Personal Information Protection Act, provided that such access may be restricted under Article 35-5 of the said act if…<br>
                  A. the access is prohibited or restricted under the law.<br>
                  B. it can pose a risk to others or cause damage to the property or interests of others.<br>
                  C. it interrupts with public agencies or organizations undertaking the following tasks.<br>
                   1) Charging or refunding taxes<br>
                   2) Academic performance evaluation or selection of applicants at schools, continuing education facilities, or any other educational systems under the Elementary and Secondary Education Act, Higher Education Act, and Lifelong Education Act.<br>
                   3) Education, skill and employment test, and qualification test <br>
                   4) Evaluation for compensation or benefit<br>
                   5) Audit or investigation under other laws<br>
                 2. Change or Removal of Personal Information: The User may ask to change or remove their personal information under Article 36 (Change &amp; Removal of Personal Information) of the Personal Information Protection Act unless otherwise specified under other applicable laws.<br>
                 3. Request for Discontinue Use of Personal Information: The User may ask for the discontinuation of the use of personal information under Article 37 (Discontinuation of Using Personal Information) of the Personal Information Protection Act unless otherwise specified under Article 37-2 of the said act if any of the following apply.<br>
                  A. It is required under special regulations or for compliance with legal obligations.<br>
                  B. It can pose a risk to others or cause damage to the property or interests of others.<br>
                  C. The personal information needs to be processed by a public organization under other applicable laws.<br>
                  D. The personal information needs to be processed to perform the service agreement, or the User has not clearly indicated their intention to terminate the service agreement.<br>
