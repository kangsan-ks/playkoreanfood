            <h2 class="category-title"><i class="ico"></i> refus de collecter des e-mails sans consentement</h2>
            <div class="terms refusal">
                <div class="txt">
                  <p>L'e-mail sur le site Web ne doit pas être utilisé par n'importe quel programme qui recueille des adresses e-mail ou être utilisé de quelque manière que ce soit sans le consentement du porteur. Si l'adresse e-mail est utilisée sans le consentement, l'utilisateur sera puni en vertu de la Loi sur l'information et les communications réseaux.</p>
                  <strong>&lt;Regulations sur la promotion d’utilisation des réseaux de communications et sur la protection des renseignements&gt; </strong>
                  <h3>L'article 50, alinéa 2 : Interdiction sur la collecte des adresses e-mail sans le consentement</h3>
                  <ol>
                    <li>① Il est interdit de collecter des adresses de courrier, en utilisant automatiquement un programme de<br> 
                    &nbsp;&nbsp;&nbsp;&nbsp;collecte de l'adresse e-mail ou tout autre moyen nécessaire, sans le consentement<br> 
                    &nbsp;&nbsp;&nbsp;&nbsp;du gérant du site internet ou l'employeur.<br>
                    &nbsp;&nbsp;&nbsp;&nbsp;&lt;Révisé 2004.12.30&gt;</li>
                    <li>② IL est interdit d'échanger ou de distribuer des adresses e-mail acquises par la violation de la<br> 
                    &nbsp;&nbsp;&nbsp;&nbsp;première clause.</li>
                    <li>③ Il est interdit d'envoyer des informations par e-mails acquis en violation de la clause de<br> 
                    &nbsp;&nbsp;&nbsp;&nbsp;premier et deuxième.<br>
                    &nbsp;&nbsp;&nbsp;&nbsp;[Ajouté le 18/12/2002]</li>
                  </ol>
            
                  <h3>L'article 50, alinéa 3 (consignation pour envoyer des publicités ou toute autre information promotionnelle pour les gains de profits)</h3>
                  <ol>
                    <li>① Toute personne qui expédie pour envoyer des publicités ou toute autre information promotionnelle<br> 
                    &nbsp;&nbsp;&nbsp;&nbsp;pour les gains de profit doit s'assurer qu'ils se conforment à l'article 50, alinéa 2.</li>
                    <li>② Conformément à l'article 1, la personne qui consigne pour envoyer des publicités ou toute autre<br> 
                    &nbsp;&nbsp;&nbsp;&nbsp;information promotionnelle pour les gains de profit est responsable de toutes pertes qui<br>
                    &nbsp;&nbsp;&nbsp;&nbsp;pourraient survenir en raison de la violation des lois.<br>
                    &nbsp;&nbsp;&nbsp;&nbsp;[Ajouté le 18/12/2002]</li>
                  </ol>
            
                  <h3>L'article 74 (la répression)</h3>
                  <ol>
                    <li>Toute personne qui a violé les clauses suivantes sera condamnée un maximum de 1 an de prison<br> 
                    &nbsp;&nbsp;&nbsp;&nbsp;ou une amende de moins de 10 millions de won.<br>
                    &lt;Revised 2005.12.30, 2007.1.26, 2007.12.21&gt;</li>
                    <li style="margin:20px 0 0">① La personne qui a violé l'article 50, alinéa 2, par la collecte, la vente, la<br>  
                    distribution, ou transférer des informations.<br>
                    &nbsp;&nbsp;&nbsp;&nbsp;[Partiellement révisée en 8667 29/02/2008]</li>
                  </ol>
