                <b>第24条（免責事項）</b><br> ① 本サービスは、天災地変、戦争その他これに準ずる不可抗力によってサービス情報が削除されたりサービスを提供することができない場合には、これに対する責任が免除されます。<br>
                ② 本サービスは、基幹通信事業者が電気通信サービスを停止したり、定期的に提供していないし、損害が発生した場合、責任が免除されます。<br>
                ③ 本サービスは、設備の補修、交換、定期点検、工事などやむを得ない事由で発生した損害に対する責任が免除されます。<br>
                ④ 本サービスは、利用者のコンピュータのエラーによって損害が発生した場合、または会員が個人情報および電子メールアドレスを不実に記載して損害が発生した場合の責任を負いません。<br>
                ⑤ 本サービスは、会員の帰責事由によるサービス利用の障害または損害に対して責任を負いません。<br>
                ⑥ 本サービスに表出された任意のコメントや情報について確認や代表する義務がなく、利用者によって投稿されたコメントを承認•反対•変更しないことにより、サービスを利用しながら得た資料による損害に対して責任をされません。<br>
                ⑦ 本サービスは、利用者またはその他の関係機関がサービスに掲載した情報について、正確性、信頼性について保証しません。<br>
                ⑧ 本サービスは、利用者がサービスを利用することによって、他の利用者のために被った精神的損害について、振興院は補償する責任を負いません。<br>
                ⑨ 本サービスは、利用者相互間及び利用者と第3者相互間にサービスを媒介として発生した紛争について介入する義務がなく、これによる損害を賠償する責任もありません。<br>
                ⑩ 本サービスからのリンクを介して提供する他のサイトの内容につきましては責任を負いません。<br>
                ⑪ ただし、振興院が個人情報保護法に違反した場合、故意または過失がないことを立証できない限り、責任が免責されることはありません。<br> <br> 
                
                <b>第25条（裁判管轄）</b><br> 振興院が提供するサービスと利用者及び利用者の間に発生した紛争に対しては、大韓民国法を適用し、紛争による訴えは大韓民国の裁判所に提起します。 <br> <br> 
                
                <b>第26条（譲渡禁止）</b><br> 会員がサービスの利用権限、その他利用契約等の地位を他人に譲渡、贈与することはできません。<br><br>  
                <b>第27条（その他の事項）</b><br> ① この約款に添付されているフォームまたは、別紙等の付属書類も規約の一部として効力を持ちます。<br>
                ② この約款に明示されていない事項や解釈異議がある事項については、一般的な慣例に基づいて協議して処理します。<br>
                ③ 上記項の協議が行われず、訴訟などの法的紛争を調整するためには、振興院所在地の管轄裁判所とします。  <br> <br>
                
