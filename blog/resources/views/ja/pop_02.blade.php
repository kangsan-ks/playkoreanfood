                <h3>第4条（情報主体の権利•義務及びその行使方法）</h3>
                <div>
                    ① 情報主体は、振興院に対して、いつでも、次の各号の個人情報保護に関連する権利を行使することができます。<br>
                    1. 個人情報の閲覧要求：「個人情報保護法」第35条（個人情報の閲覧）に基づいて閲覧を要求することができます。ただし、以下に該当する場合には、法第35条第5項により閲覧を制限することができます。<br>
                    ア．法律に基づいて閲覧が禁止または制限されている場合<br>
                    イ．他の人の生命•身体を害するおそれがある場合や、他の人の財産とその他の利益を不当に侵害するおそれがある場合<br>
                    ウ．公共機関が次の各目のいずれかに該当する業務を実行するときに重大な支障を起こす場合<br>
                    1）租税の賦課•徴収または還付に関する業務<br>
                    2）「初•中等教育法」と「高等教育法」による各学校、「生涯教育法」による生涯教育施設、その他の法律に基づいて設置された高等教育機関での成績評価または入学者選抜に関する業務<br>
                    3）学歴•技能および採用に関する試験、資格審査に関する業務<br>
                    4）補償金•給付金の算定等について、進行中の評価や判断に関する業務<br>
                    5）他の法律に基づいて進行中の監査および調査に関する業務<br>
                    2. 個人情報の訂正•削除の要求：「個人情報保護法」第36条（個人情報の訂正•削除）に応じて訂正、削除を要求することができます。ただし、他の法令では、個人情報が収集対象として明示されている場合には、その削除を要求することができません。<br>
                    3. 個人情報の処理停止要求：「個人情報保護法」第37条（個人情報の処理の停止等）に応じて、処理の停止を要求することができます。ただし、以下に該当する場合には、法第37条第2項により処理停止要求を拒絶することができます。<br>
                    ア．法律に特別な規定があるか法令上の義務を遵守するために避けられない場合<br>
                    イ．他の人の生命•身体を害するおそれがある場合や、他の人の財産とその他の利益を不当に侵害するおそれがある場合<br>
                    ウ．公共機関が個人情報を処理していなければ、他の法律で定める所管業務を行うことができない場合<br>
                    エ．個人情報を処理していないと、情報主体と約定したサービスを提供していないなど、契約の履行が困難な場合であって、情報主体が、その契約の解除の意思を明確に明らかにしなかった場合<br>
                    4. 個人情報の閲覧、訂正•削除、処理停止処理手順<br>
                    ア．個人情報の閲覧、訂正、削除、処理停止請求は、以下の手順で処理されます。<br>
