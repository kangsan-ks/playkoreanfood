                <h3>第5条（個人情報の破棄）</h3>
                <div>
                    ①振興院は、個人情報の保持期間の経過、処理目的達成などの個人情報が不要になったときは、遅滞なく、その個人情報を破棄します。<br>
                    ②個人情報破棄の手続き及び方法は次のとおりです。<br>
                    1.破棄手続き：利用者が入力した情報は、目的を達成した後、内部方針及びその他の関連法令に基づいて一定期間保存された後、あるいは、すぐに破棄されます。<br>
                    2.破棄方法：電子ファイル形式の情報は記録を再生することができない技術的方法を使用します。紙に出力された個人情報は粉砕機で粉砕を介して破棄します。<br>
                </div><br>
    
                <h3>第6条（個人情報の安全性確保措置）</h3>
                <div>
                    ① 情報主体は振興院が個人情報保護法に違反した行為で損害をもたらす振興院に損害賠償を請求することができます。この場合、振興院は故意または過失がないことを立証しなければ責任を免れるません。<br>
                    ② 振興院は、個人情報保護法第29条の規定により、次のように安全性の確保に必要な技術的/管理的および物理的な対策をしています。<br>
                    1. 内部管理計画の策定と実施<br>
                    個人情報の安全な処理のために、内部管理計画を策定して実施しています。<br>
                    2. 個人情報の取扱いの従業員の最小化と教育<br>
                    個人情報を取り扱う従業員を指定して、担当者に限定させて最小化して、個人情報を管理する対策を実施しています。<br>
                    3. 個人情報の暗号化<br>
                    利用者の個人情報とパスワードは暗号化され保存および管理されており、本人だけが知ることができ、重要なデータは、ファイルと転送データを暗号化したり、ファイルロック機能を使用するなどの別途セキュリティ機能を使用しています。<br>
                    4. ハッキングなどに備えた技術的対策<br>
                    振興院は、ハッキングやコンピュータウイルスなどによる個人情報の流出や毀損を防ぐために、セキュリティプログラムをインストールして、周期的な更新•点検をし、外部からのアクセスが制限された区域にシステムをインストールし、技術的/物理的に監視および遮断しています。<br>
                    5. 個人情報へのアクセス制限<br>
                    個人情報を処理するデータベースシステムへのアクセス権限の付与、変更、抹消を介して個人情報へのアクセス制御をするために必要な措置をしており、侵入防止システムを利用して外部からの不正なアクセスを制御しています。<br>
                    6. 接続記録の保管及び偽変造防止<br>
                    個人情報の処理システムに接続した記録を、少なくとも6ヶ月以上保管、管理しています。<br>
                    7. 文書保安のためのロックの使用<br>
                    個人情報が含まれている書類、補助記憶媒体などをロックできる安全な場所に保管しています。<br>
                    8. 非認可者に対する出入り統制<br>
                    個人情報を保持している物理的な保管場所を別に置いてこれに対してアクセス制御手順を確立し、運営しています。<br>
                    ③ 情報主体は振興院が個人情報保護法に違反した行為で損害をもたらす振興院に損害賠償を請求することができます。この場合、振興院は故意または過失がないことを立証しなければ責任を免れることはできません。<br>
                </div><br>
                
