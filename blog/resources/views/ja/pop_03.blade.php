            <h2 class="category-title"><i class="ico"></i> 電子メール無断収集拒否</h2>
            <div class="terms refusal">
                <div class="txt">
                    <p>本ウェブサイトに掲示された電子メールアドレスを、電子メール収集プログラムその他の技術的装置を利用して無断に収集することを禁止します。これに違反した時は、情報通信網法によって刑事処罰されますのでご了承ください。</p>
                    <strong>&lt;情報通信網利用促進及び情報保護などに関する法律&gt; </strong>
        
                    <h3>第50条の2 (電子メールアドレスの無断収集行為などの禁止) </h3>
                    <ol>
                        <li>① いかなる人であれインターネットホームページ運営者または管理者の事前同意なしに、インターネットホームページで<br>
                        &nbsp;&nbsp;&nbsp;自動的に電子メールアドレスを収集するプログラムその他の技術的装置を利用して、電子メールアドレスを収集して<br>
                        &nbsp;&nbsp;&nbsp;はならない。<br>
                        &nbsp;&nbsp;&nbsp;&lt;改訂 2004.12.30&gt;</li>
                        <li>② いかなる人であれ第1項の規定に違反して収集された電子メールアドレスを販売 · 流通してはならない。</li>
                        <li>③ いかなる人であれ第1項及び第2項の規定によって収集 · 販売及び流通が禁止された電子メールアドレスであることを<br>
                        &nbsp;&nbsp;&nbsp;知りながら、これを情報送信に利用してはならない。<br>&nbsp;&nbsp;&nbsp;[本条新設 2002.12.18]</li>
                    </ol>
        
                    <h3>第50条の3 (営利目的の広告性情報送信の委託など)</h3>
                    <ol>
                        <li>① 営利目的の広告性情報の送信を他人に委託した者は、当該業務を委託された者が第50条の2の規定に違反しないように<br>
                        &nbsp;&nbsp;&nbsp;管理監督しなければならない。</li>
                        <li>② 第1項の規定により、営利目的の広告性情報送信を委託された者は、当該業務と関連した法に違反して発生した損害の賠<br>
                        &nbsp;&nbsp;&nbsp;償責任において、情報送信を委託した者の所属スタッフとして見る。<br>&nbsp;&nbsp;&nbsp;[本条新設 2002.12.18]</li>
                    </ol>
        
                    <h3>第74条 (罰則)</h3>
                    <ol>
                        <li>次の各号のいずれか一つにあたる者は、1年以下の懲役または1千万ウォン以下の罰金に処する。<br>&lt;改訂 2005.12.30, 2007.1.26, 2007.12.21&gt;</li>
                        <li style="margin:20px 0 0">① 第50条の2の規定に違反して電子メールアドレスを収集・販売・流通または情報送信に利用した者。<br>&nbsp;&nbsp;&nbsp;[一部改訂 2008.02.29 法律第8667号]</li>
                    </ol>
