			⑥ 运营者∶为全盘管理服务和顺畅运营而指定的振兴院业务负责人员。<br>
			⑦ 服务使用∶使用者通过终端及与其相连接的基干通讯事业者线路输入本人的使用者ID和密码，连接至振兴院服务后截至结束为止的全部通信行为。<br>
			⑧ 服务指南∶振兴院向使用者公告使用者的使用条件及程序等管理事项的程序。<br>
			⑨ 退出∶会员结束服务使用合同的行为。<br><br><br>
			
