			<b>第5条(使用合同的成立)</b><br> ① 使用者连接至服务，经过指定的加入程序申请使用，经振兴院确认和同意后使用合同成立。<br>
			 ② 以会员ID为单位签订使用合同。<br>
			 ③ 振兴院整合运营韩餐入门网站、海外韩餐厅协议体网站、韩餐料理专业人员教育管理系统。因此，可以用同一ID和密码登录和使用振兴院整合运营的网站。振兴院在会员加入时公告整合运营并仅向对此表示同意的会员提供服务。<br>
			 ④ 同意本条款时请在申请加入时按‘同意’按钮，即可成立。<br>
			 ⑤ 振兴院有权取消符合如下各项内容的使用合同。<br>
			 1.使用他人名义申请时<br>
			 2.虚假记载使用合同申请书的内容或申请时<br>
			 3.作出妨碍他人使用服务或者盗用相应信息等的行为时<br>
			 4.使用振兴院服务做出法令和本条款禁止的行为时<br>
			 5.其他不满足振兴院规定的使用申请条件时<br><br>
