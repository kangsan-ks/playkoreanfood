			<b>第11条(在线ID的管理)</b><br> ① 使用者负责管理用于使用服务的在线ID及密码。<br> 
			 ② 振兴院通过在线ID执行管理业务，管理使用及公告板等所有使用者，因此不得变更经使用者申请后批准发行的在线ID或将其转让给他人。<br> 
