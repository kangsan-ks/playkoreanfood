                 B. 要求阅览个人信息等的方法∶通过个人信息保护综合支援网站(www.privacy.go.kr)在线申请或通过韩食振兴院离线申请。<br>
                 ②按照振兴院个人信息保护法实施规则附录第8号格式，通过书面、电子邮件、传真(FAX)等行使第1项的权利，振兴院应立即对此采取措施。<br>
                 ③信息主体要求订正或删除个人信息错误等时，振兴院截至订正或删除完成之前不得使用或提供该个人信息。<br>
                 ④可由信息主体的法定代理人和受托者等代理人行使第1项规定的权利。此时，应按照个人信息保护法实施规则附录第11号格式提交委任书。<br> <br>
                
                <b>第5条(个人信息的作废)</b><br>
                 ①振兴院应在个人信息保有期满、完成处理目的等个人信息不必要时立即作废相关个人信息。<br>
                 ②个人信息作废的程序及方法如下。<br>
                 1.作废程序∶使用者输入的信息达成目的后应按照内部方针及其他相关法令保存一定期间或者立即作废。<br>
                 2.作废方法∶用无法记录的技术方法作废电子文件形式的信息。用粉碎机粉碎的方法作废纸张打印的个人信息。<br><br>
    
                <b>第6条(确保个人信息安全性的措施) </b><br>
                 ①振兴院违反个人信息保护法的行为导致信息主体蒙受损失时，信息主体有权要求振兴院赔偿损失。振兴院无法证明不是故意或没有过失时不予免责。<br>
                 ②振兴院根据个人信息保护法第29条规定，采取如下确保安全的技术性/管理性及物理性措施。<br>
                 1. 制定并实施内部管理计划，以确保制订内部管理计划及实施个人信息的安全处理。<br>
                 2. 个人信息处理职员最少化及指定处理教育个人信息的人员，限定并最小化负责人者，执行管理个人信息的对策。<br>
                 3. 加密保存及管理个人信息加密使用者的个人信息密码，只有本人知悉的重要数据使用加密文件和传送数据或锁定文件功能等，另行使用安全功能。<br>
                 4. 预防黑客等的技术对策，振兴院为防止黑客和电脑病毒等导致个人信息泄露及毁损而设置安全程序并定期更新、检查，在控制外部接近的控制区设置系统，执行技术性/物理性的监控及切断。<br>
                 5. 向处理限制接近个人信息的个人信息数据库系统赋予、变更、删除接近权限，采取控制接近个人信息所需的措施，通过切断入侵系统，控制来自外部的任意接近。<br>
                 6. 保管访问记录及至少保管和管理六个月以上访问防止虚假/伪造个人信息处理系统的记录。<br>
                 7. 将包括使用上锁装置保护文件安全的个人信息在内的文件、辅助储存媒体等置于设有上锁装置的安全场所保管。<br>
                 8. 另设物理性保管场所，保管未经批准限制出入的个人信息，并对此制订和运营出入控制程序。<br>
                 ③振兴院违反个人信息保护法的行为导致信息主体蒙受损失时，信息主体有权要求振兴院赔偿损失。振兴院无法证明不是故意或者没有过失时不予免责。<br><br>
                
