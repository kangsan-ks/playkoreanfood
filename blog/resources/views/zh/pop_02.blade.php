                <b>第4条(信息主体的权利、义务及其行使方法)</b><br>
                ① 信息主体随时可对振兴院行使如下各项个人信息保护的相关权利。<br>
                 1.个人信息阅览要求∶可以依据《个人信息保护法》第35条(阅览个人信息)规定要求阅览。如下情况可能依据法律第35条第5项规定限制阅览。<br>
                 A. 依法禁止或限制阅览时<br>
                 B. 可能损害他人生命、身体或者可能不正当侵害他人财产、其他利益时<br>
                 C. 执行公共机构在执行如下各项业务时可能导致重大妨碍时<br>
                   1) 收取、征收或退还租税的有关业务<br>
                   2)《中小学教育法》和《高中教育法》规定的各级院校、《终身教育法》规定的终身教育设施、其他依法设立的高等教育机构评价成绩或选拔入学者的有关业务。<br>
                   3) 有关学历、技能及录用考试，审查资格的相关业务<br>
                   4) 正在评价或判断估算赔偿金、津贴等的相关业务<br>
                   5) 其他依法正在执行监查及调查的相关业务<br>
                 2.个人信息订正、删除要求∶可以根据《个人信息保护法》第36条(个人信息的订正、删除)规定要求订正和删除。但其他法令规定相应个人信息属于收集对象时，无法要求删除。<br>
                 3. 要求停止个人信息处理∶可以根据《个人信息保护法》第37条(个人信息的处理停止等)规定要求停止处理。如下情况可能依据法律第37条第2项规定，拒绝停止处理的要求。<br>
                 A. 法律有特别规定或者为遵守法令义务而无法避免的情况<br>
                 B. 可能损害他人生命、身体或者可能不正当侵害他人财产、其他利益时<br>
                 C. 公共机构不处理个人信息将无法执行其他法律规定的所管业务时<br>
                 D. 不处理个人信息将无法提供与信息主体约定的服务等难以履行合同的情况，信息主体未明确表示解除合同的意愿时<br>
                 4. 阅览、订正、删除、停止处理个人信息的处理程序<br>
                 A. 请求阅览、订正、删除、停止处理个人信息时应按照如下程序处理。<br>
