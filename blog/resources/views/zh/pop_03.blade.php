            <h2 class="category-title"><i class="ico"></i> 拒绝邮件无端收集</h2>
            <div class="terms refusal">
                <div class="txt">
                    <p>拒绝本网站揭示的电子邮件地址被电子邮件收集程序或其他技术装置任意收集，违反时根据信息通信网法受到刑事处罚，请周知。 </p>
                    <strong>&lt;关于信息通信网利用促进与信息保护等的法律&gt; </strong>
        
                    <h3>第50条之2（禁止电子邮件地址的任意收集行为等）</h3>
                    <ol>
                        <li>① 任何人在未经互联网网站运营商或管理人的事前同意之下，不可以在互联网网站利用自动收集电子邮件地址的程序或其他技术装置，收集电子邮件地址。&lt;改订2004.12.30&gt;</li>
                        <li>② 任何人不可以违反第1项规定，销售、流通所收集电子邮件地址。</li>
                        <li>③ 任何人在知晓是根据第1项与第2项规定收集•销售与流通的被禁止的电子邮件地址，而利用于信息传送。<br>&nbsp;&nbsp;&nbsp;&nbsp;[本条新设 2002.12.18]</li>
                    </ol>
        
                    <h3>第50条之3（委托盈利目的的广告性信息传送等）</h3>
                    <ol>
                        <li>① 委托他人进行盈利目的的广告性信息传送的人，需要管理监督受托该业务的人不违反第50条之2。</li>
                        <li>② 根据第1项规定，受托盈利目的的广告性信息传送的人，因为违反该业务相关法律而发生的损害赔偿方面，视为委托该信息传送的人的所属职员。<br>&nbsp;&nbsp;&nbsp;&nbsp;[本条新成 2002.12.18]</li>
                    </ol>
        
                    <h3>第74条（罚则）</h3>
                    <ol>
                        <li>属于如下各项之任何一项的人，处于1年以下的劳役或1千万韩元以下的罚金。<br>&lt;改订2005.12.30、2007.1.26、2007.12.21&gt;</li>
                        <li style="margin:20px 0 0">① 违反第50条之2，将电子邮件地址使用于收集ㆍ销售ㆍ流通或信息传送的人。<br>&nbsp;&nbsp;&nbsp;[部分改订2008.02.29法律第8667号]</li>
                    </ol>
